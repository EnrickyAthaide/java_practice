package arrays;

public class arraywithifelse{
    public static void main(String[] args){
        int[] chor= new int[4];
        chor[0]= 4; 
        chor[1]= 5; 
        chor[2]= 7; 
        chor[3]= 2; 
        if(chor[1]>chor[2])
    {
        System.out.println(chor[1] + "is greater than" + chor[2]);
    }
    else
    {
        System.out.println(chor[2] + " is greater than " + chor[1]);
    
    }
   
}
}
